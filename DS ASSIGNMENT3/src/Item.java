/**
 * class representing each item in the priority queue
 * @author Vertika
 *
 */
public class Item {
	int data;
	int priority;
	/**
	 * Parameterized constructor for item
	 * @param data is the value of the item
	 * @param priority is the priority associated with the items
	 */
	public Item(int data,int priority) {
		this.data=data;
		this.priority=priority;

	}

}
